package main

import "fmt"

// Пример 1: Массивы — это тип фиксированной длины
func fixedSizeArray() {
	// Объявление массива с фиксированной длиной
	var arr [5]int
	arr[0] = 10
	arr[1] = 20
	arr[2] = 30
	arr[3] = 40
	arr[4] = 50

	// Доступ к элементам массива
	fmt.Println("Массив фиксированного размера:", arr)
	fmt.Println("Первый элемент массива:", arr[0])
	fmt.Println("Последний элемент массива:", arr[4])
}

// Пример 2: Инициализация массива
func initializeArray() {
	// Инициализация массива с помощью литерала
	arr := [3]int{1, 2, 3}
	fmt.Println("Инициализированный массив:", arr)

	// Инициализация массива с пропущенными элементами (по умолчанию 0)
	arr2 := [5]int{1, 2}
	fmt.Println("Инициализированный массив с пропущенными значениями:", arr2)
}

// Пример 3: Массивы передаются по значению
func passByValue() {
	// Объявление массива
	arr := [3]int{10, 20, 30}
	fmt.Println("Изначальный массив:", arr)

	// Функция, которая принимает массив и изменяет его
	changeArray(arr)
	fmt.Println("Массив после передачи по значению (не изменен):", arr)
}

// Функция, которая принимает массив и изменяет его элементы
func changeArray(arr [3]int) {
	arr[0] = 100
	fmt.Println("Массив внутри функции (измененный):", arr)
}

// Пример 4: Индексация и работа с элементами массива
func arrayIndexing() {
	// Объявление и инициализация массива
	arr := [4]string{"apple", "banana", "cherry", "date"}

	// Доступ к элементам массива по индексу
	fmt.Println("Первый элемент массива:", arr[0])
	fmt.Println("Третий элемент массива:", arr[2])

	// Изменение элемента массива
	arr[1] = "blueberry"
	fmt.Println("Массив после изменения второго элемента:", arr)

	// Проход по массиву с помощью обычного цикла
	for i := 0; i < len(arr); i++ {
		fmt.Printf("Элемент %d: %s\n", i, arr[i])
	}

	// Проход по массиву с помощью range
	for index, value := range arr {
		fmt.Printf("Индекс: %d, Значение: %s\n", index, value)
	}
}

func main() {
	// Пример 1: Массивы — это тип фиксированной длины
	fixedSizeArray()

	// Пример 2: Инициализация массива
	initializeArray()

	// Пример 3: Массивы передаются по значению
	passByValue()

	// Пример 4: Индексация и работа с элементами массива
	arrayIndexing()
}
